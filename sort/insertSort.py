# 插入排序的思想：
# 将一个记录插入到已排好的有序表中，从而得到一个新的，记录数增1的有序表。

def insertSort(lists):
    count = len(lists)
    for i in range(1,count):
        j = i - 1
        key = lists[i]
        while j >= 0:
            if key < lists[j]:
                lists[j + 1] = lists[j]
                lists[j] = key
                print(lists)
            j = j - 1
    return lists

